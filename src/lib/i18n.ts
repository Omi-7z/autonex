export type Language = keyof typeof translations;
export const LANGUAGES: { key: Language; label: string }[] = [
    { key: 'en', label: 'EN' },
    { key: 'es', label: 'ES' },
    { key: 'zh', label: '中文' },
];
// Utility to get nested translations safely
export function getTranslator(lang: Language) {
  return function t(key: string, replacements?: Record<string, string | number>): any {
    const keys = key.split('.');
    let result: any = translations[lang];
    for (const k of keys) {
      result = result?.[k];
      if (result === undefined) {
        // Fallback to English if key not found in current language
        let fallbackResult: any = translations.en;
        for (const fk of keys) {
            fallbackResult = fallbackResult?.[fk];
            if (fallbackResult === undefined) return key;
        }
        result = fallbackResult;
        break;
      }
    }
    if (typeof result === 'string' && replacements) {
      return Object.entries(replacements).reduce((acc, [k, v]) => {
        return acc.replace(`{${k}}`, String(v));
      }, result);
    }
    return result ?? key;
  };
}